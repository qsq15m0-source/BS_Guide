function renderList(arr){
  let html="";
  arr.forEach(b=>{
    html+=`<div class="brawler" onclick="openGuide('${b.id}')">
      <img src="${b.img}"> 
      ${b.name}
    </div>`;
  });
  document.getElementById("list").innerHTML=html;
}

function search(text){
  renderList(brawlers.filter(b=>b.name.toLowerCase().includes(text.toLowerCase())));
}

